# arithmetic operators are used with numeric values to perform common mathematical operations

x = 9
y = 2
z = x % y
print(z)

# assignment operators are used to assign values to variable

# comparison operators are used to compare two or more values

print(x==y)

print(x!=y)

print(y>x)

#Logical operators are used to combine conditional statements
#and returns true if both statements are true

print(x==y and y<20)  #false

#or returns true if one of the statement  true

print(x==y or y<20)   #true

#not reverses the result obtained, returns false if result is true

print(not(x==y or y<20))

